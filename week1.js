function get_array() {
	var arr = [];
	for (var i = 1; i <= 255; i++) {
		arr.push(i);
	}
	return arr;
}

function sum_even_numbers(){
	var sum = 0;
	for (var i = 1; i <= 1000; i++){
    	if (i % 2 === 0) {
    		sum += i;
    	}
  	}
	return sum;
}

function sum_odd_numbers() {
    var sum = 0;
    for (var i = 0; i <= 5000; i++){
    	if (i % 2 !== 0) {
    		sum += i;
    	}
    }
    return sum;
}
